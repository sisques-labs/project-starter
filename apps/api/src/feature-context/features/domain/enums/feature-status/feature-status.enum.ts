/**
 * FeatureStatusEnum represents the status of a feature.
 * ACTIVE: The feature is active and available for use.
 * INACTIVE: The feature is inactive and not available.
 * DEPRECATED: The feature is deprecated and will be removed in the future.
 */
export enum FeatureStatusEnum {
  ACTIVE = 'ACTIVE',
  INACTIVE = 'INACTIVE',
  DEPRECATED = 'DEPRECATED',
}
